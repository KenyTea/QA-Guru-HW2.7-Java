package guru.qa;

public class Human {
    private String name;
    private String surname;
    private int age;

    public String GetName(){
        return name;
    }
    public void SetName(String name){
        this.name = name;
    }


    public String GetSurname(){
        return surname;
    }
    public void SetSurname(String surname){
        this.surname = surname;
    }


    public int GetAge(){
        return age;
    }
    public void SetAge(int age){
        this.age = age;
    }

}
